
public class _2_Operators {

	public static void main(String[] args) {
		int a = 100, b = 20;
		int sum = a + b;
		int sub = a - b;
		int mul = a * b;
		int div = a / b;
		int mod = a % b;
		int incr = ++a;
		int decr = --b;
		int and = a & 3;
		int or = a | 3;
		int power = 5 ^ 2;
		System.out.println("Sum: " + sum);
		System.out.println("Sub: " + sub);
		System.out.println("Multi: " + mul);
		System.out.println("Division: " + div);
		System.out.println("Moddulas: " + mod);

		System.out.println("Increment: " + incr);
		System.out.println("Decrement: " + decr);

		System.out.println("AND: " + and);
		System.out.println("OR: " + or);
		System.out.println("Power: " + power);

	}
}
